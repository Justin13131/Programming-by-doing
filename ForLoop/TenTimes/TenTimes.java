import java.util.*;

public class TenTimes {
    public static void main (String[] args){
        int line = 1;
        //Output 10 times
        for (int n = 1; n <= 10; n++) {
            System.out.println(line +". Mr. Mitchell is cool");
            line++;
        }
    }

}
